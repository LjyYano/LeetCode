#!/bin/bash

# https://leetcode.cn/problems/valid-phone-numbers/
# 
# 给定一个包含电话号码列表（一行一个电话号码）的文本文件 file.txt，写一个单行 bash 脚本输出所有有效的电话号码。
# 
# 你可以假设一个有效的电话号码必须满足以下两种格式之一：
# 1. (xxx) xxx-xxxx
# 2. xxx-xxx-xxxx
# 其中 x 表示一个数字。
# 
# 你也可以假设每行前后没有多余的空格字符。
# 
# 示例：
# 假设 file.txt 内容如下：
# 987-123-4567
# 123 456 7890
# (123) 456-7890
# 
# 你的脚本应当输出下列有效的电话号码：
# 987-123-4567
# (123) 456-7890

# 使用 grep 命令匹配两种格式的电话号码
grep -E '^([0-9]{3}-|\([0-9]{3}\) )[0-9]{3}-[0-9]{4}$' file.txt 