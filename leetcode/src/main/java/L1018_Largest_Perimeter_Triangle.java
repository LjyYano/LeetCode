import java.util.Arrays;

// https://leetcode-cn.com/problems/largest-perimeter-triangle/
class L1018_Largest_Perimeter_Triangle {
    public int largestPerimeter(int[] A) {
        Arrays.sort(A);
        for (int i = A.length - 1; i >= 2; i--) {
            if (A[i - 2] + A[i - 1] > A[i]) {
                return A[i - 2] + A[i - 1] + A[i];
            }
        }
        return 0;
    }
}